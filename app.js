function getDatasets() {
  const datasets = [];
  selectedCompanies.forEach(name => {
    const info = COMPANY_DATA[name];
    if (!info) return;

    const metricKey = currentMetric === 'revenue' ? 'revenuePerEmployee' : 'profitPerEmployee';
    const filteredData = info.data.filter(d => {
      const val = d[metricKey];
      if (val == null) return false;
      if (xAxisMode === 'founding') {
        return d.yearsSinceFounding >= rangeMin && d.yearsSinceFounding <= rangeMax;
      } else {
        return d.year >= rangeMin && d.year <= rangeMax;
      }
    });

    datasets.push({
      label: name,
      data: filteredData.map(d => {
        const mult = inflationAdjust ? inflationMultiplier(d.year) : 1;
        const yVal = d[metricKey] * mult;
        return {
          x: xAxisMode === 'founding' ? d.yearsSinceFounding : d.year,
          y: yVal,
          // store both nominal and real values for tooltip
          nominalRevPerEmp: d.revenuePerEmployee,
          nominalProfitPerEmp: d.profitPerEmployee,
          realRevPerEmp: d.revenuePerEmployee * mult,
          realProfitPerEmp: d.profitPerEmployee != null ? d.profitPerEmployee * mult : null,
          inflationMult: mult,
          ...d
        };
      }),
      borderColor: info.color,
      backgroundColor: info.color + '20',
      borderWidth: info.isPrivate ? 2 : 2.5,
      borderDash: info.isPrivate ? [6, 3] : [],
      pointRadius: 0,
      pointHoverRadius: 5,
      pointHoverBackgroundColor: info.color,
      pointHoverBorderColor: '#0a0a0f',
      pointHoverBorderWidth: 2,
      tension: 0.3,
      fill: false,
      order: info.isSegment ? 1 : 0
    });
  });
  return datasets;
}

function updateChart() {
  if (!chart) return;
  
  const isLog = document.getElementById('logScaleToggle').checked;
  
  chart.data.datasets = getDatasets();
  chart.options.scales.x.min = rangeMin;
  chart.options.scales.x.max = rangeMax;
  chart.options.scales.x.title.text = xAxisMode === 'founding' ? 'YEARS SINCE FOUNDING' : 'CALENDAR YEAR';
  chart.options.scales.x.ticks.stepSize = xAxisMode === 'founding' ? 5 : 5;
  chart.options.scales.y.type = isLog ? 'logarithmic' : 'linear';
  chart.options.scales.y.title.text = currentMetric === 'revenue' ? 'REVENUE PER EMPLOYEE' : 'PROFIT PER EMPLOYEE';
  
  if (isLog) {
    chart.options.scales.y.ticks.callback = formatDollarAxis;
  } else {
    chart.options.scales.y.ticks.callback = formatDollarAxis;
  }
  
  chart.update('none');
  
  // Update header
  document.getElementById('chartTitle').textContent = 
    currentMetric === 'revenue' ? 'REVENUE PER EMPLOYEE' : 'PROFIT PER EMPLOYEE';
  document.getElementById('chartBadge').textContent = 
    inflationAdjust ? '2025 USD' : 'NOMINAL USD';
}

