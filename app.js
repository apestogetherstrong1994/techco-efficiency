function updateTable() {
  const tbody = document.getElementById('tableBody');
  tbody.innerHTML = '';

  const metricKey = currentMetric === 'revenue' ? 'revenuePerEmployee' : 'profitPerEmployee';
  const sortLabel = currentMetric === 'revenue' ? 'SORTED BY REVENUE/EMP' : 'SORTED BY PROFIT/EMP';
  document.getElementById('tableBadge').textContent = sortLabel + (inflationAdjust ? ' (2025$)' : '');

  const rows = [];
  selectedCompanies.forEach(name => {
    const info = COMPANY_DATA[name];
    if (!info || info.data.length === 0) return;
    const latest = info.data[info.data.length - 1];
    const mult = inflationAdjust ? inflationMultiplier(latest.year) : 1;
    const catLabel = info.category === 'm7' ? 'M7' : info.category === 'segments' ? 'SEGMENT' : 'OTHER';
    rows.push({ name, info, latest, catLabel, mult });
  });

  rows.sort((a, b) => {
    const va = (a.latest[metricKey] || 0) * a.mult;
    const vb = (b.latest[metricKey] || 0) * b.mult;
    return vb - va;
  });

  rows.forEach(({ name, info, latest, catLabel, mult }) => {
    const tr = document.createElement('tr');
    const revPerEmp = latest.revenuePerEmployee * mult;
    const profPerEmp = latest.profitPerEmployee != null ? latest.profitPerEmployee * mult : null;
    
    tr.innerHTML = `
      <td>
        <div class="table-company-cell">
          <span class="table-swatch" style="background:${info.color}"></span>
          <span class="table-company-name">${displayName(name)}</span>
          ${info.isPrivate ? '<span class="company-private-badge">EST</span>' : ''}
        </div>
      </td>
      <td class="td-category">${catLabel}</td>
      <td>${info.founded}</td>
      <td>${latest.year}</td>
      <td>${latest.yearsSinceFounding}</td>
      <td>${formatBillions(latest.revenue_b)}</td>
      <td class="${latest.profit_b != null ? (latest.profit_b >= 0 ? 'td-positive' : 'td-negative') : ''}">${formatBillions(latest.profit_b)}</td>
      <td>${formatEmployees(latest.employees)}</td>
      <td class="td-positive">${formatDollar(revPerEmp)}</td>
      <td class="${profPerEmp != null ? (profPerEmp >= 0 ? 'td-positive' : 'td-negative') : ''}">${formatDollar(profPerEmp)}</td>
    `;
    tbody.appendChild(tr);
  });
}

