function buildCompanyList(containerId, companies) {
  const container = document.getElementById(containerId);
  container.innerHTML = '';
  companies.forEach(name => {
    const info = COMPANY_DATA[name];
    const isActive = selectedCompanies.has(name);
    const latestData = info.data[info.data.length - 1];
    const mult = inflationAdjust ? inflationMultiplier(latestData?.year) : 1;
    let latestVal = currentMetric === 'revenue' 
      ? latestData?.revenuePerEmployee 
      : latestData?.profitPerEmployee;
    if (latestVal != null) latestVal = latestVal * mult;

    const item = document.createElement('div');
    item.className = `company-item${isActive ? ' active' : ''}`;
    item.onclick = () => toggleCompany(name);

    let badges = '';
    if (info.isPrivate) {
      badges = '<span class="company-private-badge">EST</span>';
    }

    item.innerHTML = `
      <input type="checkbox" ${isActive ? 'checked' : ''}>
      <span class="company-swatch" style="background:${info.color}; color:${info.color}"></span>
      <span class="company-name">${displayName(name)}</span>
      ${badges}
      <span class="company-latest">${latestVal != null ? formatDollar(latestVal) : '—'}</span>
    `;
    container.appendChild(item);
  });
}

function toggleCompany(name) {
  if (selectedCompanies.has(name)) {
    selectedCompanies.delete(name);
  } else {
    selectedCompanies.add(name);
  }
  refreshAll();
}

