function setupEventListeners() {
  // Metric tabs
  document.getElementById('tab-revenue').addEventListener('click', () => {
    currentMetric = 'revenue';
    document.getElementById('tab-revenue').classList.add('active');
    document.getElementById('tab-profit').classList.remove('active');
    refreshAll();
  });
  
  document.getElementById('tab-profit').addEventListener('click', () => {
    currentMetric = 'profit';
    document.getElementById('tab-profit').classList.add('active');
    document.getElementById('tab-revenue').classList.remove('active');
    refreshAll();
  });

  // X-axis mode toggle
  document.getElementById('xaxis-founding').addEventListener('click', () => {
    if (xAxisMode === 'founding') return;
    xAxisMode = 'founding';
    document.getElementById('xaxis-founding').classList.add('active');
    document.getElementById('xaxis-calendar').classList.remove('active');
    document.getElementById('headerSubtitle').innerHTML = 'Revenue & Profit per Employee &mdash; Normalized by Years Since Founding';
    switchRangeSlider();
    refreshAll();
  });

  document.getElementById('xaxis-calendar').addEventListener('click', () => {
    if (xAxisMode === 'calendar') return;
    xAxisMode = 'calendar';
    document.getElementById('xaxis-calendar').classList.add('active');
    document.getElementById('xaxis-founding').classList.remove('active');
    document.getElementById('headerSubtitle').innerHTML = 'Revenue & Profit per Employee &mdash; By Calendar Year';
    switchRangeSlider();
    refreshAll();
  });

  // Inflation adjustment
  document.getElementById('inflationToggle').addEventListener('change', (e) => {
    inflationAdjust = e.target.checked;
    refreshAll();
  });

  // Log scale
  document.getElementById('logScaleToggle').addEventListener('change', () => {
    updateChart();
  });

  // Reset zoom
  document.getElementById('resetZoomBtn').addEventListener('click', () => {
    if (chart) chart.resetZoom();
  });

  // Category toggles
  ['m7', 'segments', 'other'].forEach(cat => {
    document.getElementById(`cat-${cat}`).addEventListener('change', (e) => {
      const companies = CATEGORIES[cat];
      if (e.target.checked) {
        companies.forEach(c => selectedCompanies.add(c));
      } else {
        companies.forEach(c => selectedCompanies.delete(c));
      }
      refreshAll();
    });
  });

  // Select All / Deselect All
  document.getElementById('selectAll').addEventListener('click', () => {
    Object.keys(COMPANY_DATA).forEach(name => selectedCompanies.add(name));
    refreshAll();
  });

  document.getElementById('deselectAll').addEventListener('click', () => {
    selectedCompanies.clear();
    refreshAll();
  });

  // Range sliders
  const rangeMinEl = document.getElementById('rangeMin');
  const rangeMaxEl = document.getElementById('rangeMax');
  
  function updateRange() {
    let min = parseInt(rangeMinEl.value);
    let max = parseInt(rangeMaxEl.value);
    if (min > max - 2) {
      if (this === rangeMinEl) {
        min = max - 2;
        rangeMinEl.value = min;
      } else {
        max = min + 2;
        rangeMaxEl.value = max;
      }
    }
    rangeMin = min;
    rangeMax = max;
    document.getElementById('rangeMinVal').textContent = min;
    document.getElementById('rangeMaxVal').textContent = max;
    
    // Update track highlight
    const sliderMin = parseInt(rangeMinEl.min);
    const sliderMax = parseInt(rangeMinEl.max);
    const pctMin = ((min - sliderMin) / (sliderMax - sliderMin)) * 100;
    const pctMax = ((max - sliderMin) / (sliderMax - sliderMin)) * 100;
    const track = document.getElementById('rangeTrack');
    track.style.background = `linear-gradient(to right, #1e1e32 ${pctMin}%, #00e5a0 ${pctMin}%, #00e5a0 ${pctMax}%, #1e1e32 ${pctMax}%)`;
    
    // Update range label
    document.getElementById('rangeLabel').textContent = xAxisMode === 'founding' ? 'YEARS SINCE FOUNDING' : 'CALENDAR YEAR';
    document.getElementById('rangePrefix').textContent = xAxisMode === 'founding' ? 'Y' : '';
    document.getElementById('rangePrefix2').textContent = xAxisMode === 'founding' ? 'Y' : '';
    
    updateChart();
  }

  // Make updateRange accessible for switchRangeSlider
  window._updateRange = updateRange;
  window._rangeMinEl = rangeMinEl;
  window._rangeMaxEl = rangeMaxEl;

  rangeMinEl.addEventListener('input', updateRange);
  rangeMaxEl.addEventListener('input', updateRange);
  
  // Initial track
  updateRange.call(rangeMinEl);
  
  // Initialize category checkboxes
  updateCategoryCheckboxes();
}

