function switchRangeSlider() {
  const rangeMinEl = window._rangeMinEl || document.getElementById('rangeMin');
  const rangeMaxEl = window._rangeMaxEl || document.getElementById('rangeMax');
  
  if (xAxisMode === 'founding') {
    rangeMinEl.min = FOUNDING_MIN;
    rangeMinEl.max = FOUNDING_MAX;
    rangeMaxEl.min = FOUNDING_MIN;
    rangeMaxEl.max = FOUNDING_MAX;
    rangeMinEl.value = FOUNDING_MIN;
    rangeMaxEl.value = FOUNDING_MAX;
    rangeMin = FOUNDING_MIN;
    rangeMax = FOUNDING_MAX;
  } else {
    rangeMinEl.min = CALENDAR_MIN;
    rangeMinEl.max = CALENDAR_MAX;
    rangeMaxEl.min = CALENDAR_MIN;
    rangeMaxEl.max = CALENDAR_MAX;
    rangeMinEl.value = CALENDAR_MIN;
    rangeMaxEl.value = CALENDAR_MAX;
    rangeMin = CALENDAR_MIN;
    rangeMax = CALENDAR_MAX;
  }
  
  document.getElementById('rangeMinVal').textContent = rangeMin;
  document.getElementById('rangeMaxVal').textContent = rangeMax;
  document.getElementById('rangeLabel').textContent = xAxisMode === 'founding' ? 'YEARS SINCE FOUNDING' : 'CALENDAR YEAR';
  document.getElementById('rangePrefix').textContent = xAxisMode === 'founding' ? 'Y' : '';
  document.getElementById('rangePrefix2').textContent = xAxisMode === 'founding' ? 'Y' : '';
  
  // Update track
  const track = document.getElementById('rangeTrack');
  track.style.background = `linear-gradient(to right, #1e1e32 0%, #00e5a0 0%, #00e5a0 100%, #1e1e32 100%)`;
}

